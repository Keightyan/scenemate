<script>
(function() {
    const locations = @json($locations);
    const prefName = '{{ $prefectureName }}';
    const fnName = 'updateCities_' + prefName.replace(/-/g, '_');

    window[fnName] = function(prefecture) {
        const citySelect = document.getElementById('city-select-' + prefName);
        citySelect.innerHTML = '<option value="">選択してください</option>';
        if (prefecture && locations[prefecture]) {
            locations[prefecture].forEach(city => {
                const opt = document.createElement('option');
                opt.value = city;
                opt.textContent = city;
                citySelect.appendChild(opt);
            });
        }
    };
})();
</script>
